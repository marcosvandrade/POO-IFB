package exemploHeitorEmail.Aula;

public final class Matematica {
	
	public final static double PI = 3.1413;
	
	public static double media(double num1, double num2) {
		return (num1 + num2) / 2;
	}
	
	public double media(double num1, double num2, double num3) {
		return (num1 + num2 + num3) / 3;
	}
	
	public static double aoQuadrado(double num) {
		return num * num;
	}
	
}
